

public class Card {
//Attributes

    private int power;
    private String suit;



// methods

//getter & setter
    public int getPower() {
        return power;
    }

    public void setPower(int power) {
        this.power = power;
    }

    public String getSuit() {
        return suit;
    }

    public void setSuit(String suit) {
        this.suit = suit;
    }




}
